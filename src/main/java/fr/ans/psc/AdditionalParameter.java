package fr.ans.psc;

public class AdditionalParameter {
    private String name;

    private String value;

    public AdditionalParameter() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
